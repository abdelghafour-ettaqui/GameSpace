package playStation;

public class Game {
    private String name;
    private String type;
    private String postWorksOn;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getPostWorksOn() {
        return postWorksOn;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setPostWorksOn(String postWorksOn) {
        this.postWorksOn = postWorksOn;
    }
}
